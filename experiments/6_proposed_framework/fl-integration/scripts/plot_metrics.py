#!/usr/bin/env python3
"""
fl-integration/scripts/plot_metrics.py
Parses structured JSON logs generated by the HCFL Framework and generates
evaluation metric plots (PR-AUC, ROC-AUC, F1, Latency).
"""
import os
import json
import argparse
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns


def parse_evaluation_logs(log_path: str) -> pd.DataFrame:
    metrics = []
    with open(log_path, 'r') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                record = json.loads(line)
                # Parse Bank HQ Evaluation logs
                if "pr_auc" in record and "round_num" in record and "bank_id" in record:
                    metrics.append({
                        "round_num": record["round_num"],
                        "bank_id": record["bank_id"],
                        "pr_auc": record["pr_auc"],
                        "roc_auc": record.get("roc_auc", 0.5),
                        "f1": record.get("f1", 0.0)
                    })
            except json.JSONDecodeError:
                pass
    return pd.DataFrame(metrics)


def parse_latency_logs(log_path: str) -> pd.DataFrame:
    latencies = []
    with open(log_path, 'r') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                record = json.loads(line)
                # Parse Round Coordinator completion logs containing elapsed
                msg = record.get("message", "")
                if record.get("component") == "round_coordinator" and "elapsed=" in msg:
                    # Extract elapsed from message string (e.g. elapsed=1.5s)
                    import re
                    match = re.search(r"elapsed=([0-9\.]+)s", msg)
                    if match:
                        latencies.append({
                            "round_num": record.get("round_num", 0),
                            "latency_sec": float(match.group(1))
                        })
            except json.JSONDecodeError:
                pass
    return pd.DataFrame(latencies)


def generate_plots(eval_df: pd.DataFrame, latency_df: pd.DataFrame, output_dir: str):
    os.makedirs(output_dir, exist_ok=True)
    sns.set_theme(style="whitegrid")

    # 1. Bank-specific PR-AUC
    if not eval_df.empty:
        plt.figure(figsize=(10, 6))
        sns.lineplot(data=eval_df, x="round_num", y="pr_auc", hue="bank_id", marker="o")
        plt.title("PR-AUC Progression per Bank")
        plt.xlabel("Round Number")
        plt.ylabel("PR-AUC")
        plt.ylim(0, 1.05)
        out_path = os.path.join(output_dir, "pr_auc_per_bank.png")
        plt.savefig(out_path)
        plt.close()
        print(f"Saved: {out_path}")

        # 2. Global Averaged Metrics (PR-AUC, ROC-AUC, F1)
        avg_df = eval_df.groupby("round_num")[["pr_auc", "roc_auc", "f1"]].mean().reset_index()
        plt.figure(figsize=(10, 6))
        sns.lineplot(data=avg_df, x="round_num", y="pr_auc", label="Avg PR-AUC", marker="o")
        sns.lineplot(data=avg_df, x="round_num", y="roc_auc", label="Avg ROC-AUC", marker="s")
        sns.lineplot(data=avg_df, x="round_num", y="f1", label="Avg F1 Score", marker="^")
        plt.title("Global Average Evaluation Metrics over Rounds")
        plt.xlabel("Round Number")
        plt.ylabel("Score")
        plt.ylim(0, 1.05)
        plt.legend()
        out_path = os.path.join(output_dir, "global_avg_metrics.png")
        plt.savefig(out_path)
        plt.close()
        print(f"Saved: {out_path}")

    # 3. Round Latency
    if not latency_df.empty:
        plt.figure(figsize=(10, 6))
        sns.barplot(data=latency_df, x="round_num", y="latency_sec", color="skyblue")
        plt.title("Coordinator Round Completion Latency")
        plt.xlabel("Round Number")
        plt.ylabel("Latency (seconds)")
        out_path = os.path.join(output_dir, "round_latency.png")
        plt.savefig(out_path)
        plt.close()
        print(f"Saved: {out_path}")


def main():
    parser = argparse.ArgumentParser(description="Plot HCFL metrics from structured JSON logs.")
    parser.add_argument("--log_file", type=str, required=True, help="Path to the JSON log file (e.g., api_server.log)")
    parser.add_argument("--out_dir", type=str, default="plots", help="Directory to save the generated plots")
    args = parser.parse_args()

    print(f"Parsing logs from {args.log_file}...")
    eval_df = parse_evaluation_logs(args.log_file)
    latency_df = parse_latency_logs(args.log_file)

    if eval_df.empty and latency_df.empty:
        print("No metrics found in the provided log file.")
        return

    generate_plots(eval_df, latency_df, args.out_dir)


if __name__ == "__main__":
    main()
